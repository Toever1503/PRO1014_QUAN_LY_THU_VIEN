    void fillTable(List<HoiVien> hvd) {
        DefaultTableModel model = (DefaultTableModel) tblQuanLyNguoiDung.getModel();
        model.setRowCount(0);
        if (hvd != null) {
            try {
                for (HoiVien hv : hvd) {
                    Object[] row = {
                        hv.getId(),
                        hv.getFullName(),
                        hv.getSoDienThoai(),
                        hv.getEmail(),
                        hv.getDiaChi(),
                        hv.isTrangThai() ? "Mở" : "Khóa"
                    };
                    model.addRow(row);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Lỗi truy vấn dữ liệu!");
            }
        }
    }

