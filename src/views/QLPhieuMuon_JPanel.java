    public boolean addSachMuon(PhieuMuonChiTiet pmct, Sach sach) {
        if (listSachMuon.containsKey(sach.getId())) {
            return false;
        } else {
            listSachMuon.put(sach.getId(), pmct);
            tableModelSachMuon.addRow(new Object[]{
                sach.getId().toString(), sach.getTenSach(),
                null,
                pmct.getNguoiXuLy(),
                "Mới"
            });
            return true;
        }
    }

