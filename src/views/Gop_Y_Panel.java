    private void btnLast1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLast1ActionPerformed
        // TODO add your handling code here:
        pageIndex = total;
        List<GopY> list = listGopY.get(pageIndex);
        if (list == null) {
            list = gopYDAO.selectByPage(Long.valueOf(pageIndex));
            listGopY.put(pageIndex, list);
        }
        fillTableGopY(list);
    }//GEN-LAST:event_btnLast1ActionPerformed

    private void btnNext1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNext1ActionPerformed
        // TODO add your handling code here:
        if (pageIndex + 1 > total) {
            JOptionPane.showConfirmDialog(this, "Bạn hiện đã ở trang cuối");
        } else {
            pageIndex++;
            List<GopY> list = listGopY.get(pageIndex);
            if (list == null) {
                list = gopYDAO.selectByPage(Long.valueOf(pageIndex));
                listGopY.put(pageIndex, list);
            }
            fillTableGopY(list);
        }
    }//GEN-LAST:event_btnNext1ActionPerformed

    private void btnPrev1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrev1ActionPerformed
        // TODO add your handling code here:
        if (pageIndex - 1 < 0) {
            JOptionPane.showConfirmDialog(this, "Bạn hiện đã ở trang đầu");
        } else {
            pageIndex--;
            List<GopY> list = listGopY.get(pageIndex);
            if (list == null) {
                list = gopYDAO.selectByPage(Long.valueOf(pageIndex));
                listGopY.put(pageIndex, list);
            }
            fillTableGopY(list);
        }
    }//GEN-LAST:event_btnPrev1ActionPerformed

