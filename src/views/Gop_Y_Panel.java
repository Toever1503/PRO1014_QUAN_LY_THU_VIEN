    private void fillTableGopY(List<GopY> list) {
        tableModelGopY.setRowCount(0);
        list.forEach(gopY -> {
            tableModelGopY.addRow(new Object[]{
                gopY.getId(), gopY.getNoiDung(), gopY.getSoLuot()
            });
        });
    }
