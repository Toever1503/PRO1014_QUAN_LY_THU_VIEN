    private void btnThemMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemMoiActionPerformed
        // TODO add your handling code here:
        String s = JOptionPane.showInputDialog(this, "Nhập tên đề xuất:", "Thêm đề xuất", JOptionPane.PLAIN_MESSAGE);
        if (s != null) {
            GopY gopY = new GopY();
            gopY.setNoiDung(s);
            gopY.setNgayTao(new Date(Calendar.getInstance().getTimeInMillis()));
            gopY.setTrangThai(Boolean.TRUE);
            if (gopYDAO.insertOnUpdate(gopY) > 0) {
                JOptionPane.showMessageDialog(this, "Thêm thành công!");
                pageIndex = 0;
                listGopY.clear();
                listGopY.put(pageIndex, gopYDAO.selectByPage(Long.valueOf(0)));
                fillTableGopY(listGopY.get(pageIndex));
            } else {
                JOptionPane.showMessageDialog(this, "Lỗi Server, code: -1");
            }
        }
    }//GEN-LAST:event_btnThemMoiActionPerformed

    private void btnIangDeXuatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIangDeXuatActionPerformed
        // TODO add your handling code here:
        int row = tblDeXuat.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Hãy chọn 1 góp ý");
        } else {
            GopY gopY = listGopY.get(pageIndex).get(row);
            gopY.setSoLuot(gopY.getSoLuot() + 1);
            if (gopYDAO.insertOnUpdate(gopY) > 0) {
                JOptionPane.showMessageDialog(this, "Tăng thành công!");
                listGopY.get(pageIndex).set(row, gopY);
                tableModelGopY.setValueAt(gopY.getSoLuot(), row, 2);
            } else {
                JOptionPane.showMessageDialog(this, "Lỗi Server, code: -1");
            }
        }
    }//GEN-LAST:event_btnIangDeXuatActionPerformed

