    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        String search = txtSearch.getText();
        if (search.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Hãy nhập nội dung cần tìm!");
        } else {
            search = "%".concat(search).concat("%");
            List<GopY> list = gopYDAO.selectByKeyword(search);
            if (list.size() != 0) {
                fillTableGopY(list);
            } else {
                JOptionPane.showMessageDialog(this, "Không tìm thấy!");
            }

        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

    private void btnInactiveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInactiveActionPerformed
        // TODO add your handling code here:
        int row = tblDeXuat.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Hãy chọn 1 góp ý");
        } else {
            GopY gopY = listGopY.get(pageIndex).get(row);
            gopY.setTrangThai(Boolean.FALSE);
            if (gopYDAO.insertOnUpdate(gopY) > 0) {
                JOptionPane.showMessageDialog(this, "Inactive thành công!");
                listGopY.get(pageIndex).set(row, gopY);
                tableModelGopY.setValueAt(gopY.getSoLuot(), row, 2);
            } else {
                JOptionPane.showMessageDialog(this, "Lỗi Server, code: -1");
            }
        }
    }//GEN-LAST:event_btnInactiveActionPerformed


